1.1 设计目的
    本毕业设计是一款使用Godot引擎与GDScript语言、基于MVC程序框架进行开发、像素风格的使用了多玩法叙事的角色扮演游戏。
    其目的在于，通过游戏讲述一个关于刚毕业初入社会的大学生在各方面上屡屡受挫，被迫接受各种不合理，
    并在经历了各种事情之后重新认识自己接纳自己，最后选择了勇敢面对自己的内心，向着自己的梦想前进的故事，来让玩家感受到角色的内心从负面到积极的转变。

1.2 设计意义
    本毕业设计的意义在于通过游戏来表达角色的转变以激励大家对于生活的热情和未来的自信。
    随着被称为“第九艺术”的游戏的发展，越来越多的人通过游戏来表达自己的想法，也有越来越多的人通过游戏来获得新的思考，
    而电子游戏的主要受众的大学生，正是本作品想反应的有着普遍迷茫不自信的问题的将入社会或初入社会的年轻人群体，
    所以除开选择的游戏载体所使用的技术所能呈现的更好的效果以外，也更加贴合受众。